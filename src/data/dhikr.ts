// دوال مساعدة للتعامل مع البيانات
export const getDhikrByCategory = (category: string) => {
  return adhkar.filter(dhikr => dhikr.category === category);
};

export const searchDhikr = (query: string) => {
  return adhkar.filter(dhikr => 
    dhikr.arabic.includes(query) || 
    dhikr.categoryName.includes(query)
  );
};

export const getFavoriteDhikr = () => {
  const favorites = localStorage.getItem('favoriteDhikr');
  return favorites ? JSON.parse(favorites) : [];
};

export const toggleFavorite = (dhikrId: number) => {
  const favorites = getFavoriteDhikr();
  const index = favorites.indexOf(dhikrId);
  
  if (index > -1) {
    favorites.splice(index, 1);
  } else {
    favorites.push(dhikrId);
  }
  
  localStorage.setItem('favoriteDhikr', JSON.stringify(favorites));
  return favorites;
};